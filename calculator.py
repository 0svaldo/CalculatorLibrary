"""
Libreria para el calculo de operaciones matematicas basicas
"""

def add(first_term, second_term):
    return first_term + second_term

def substract(first_term, second_term):
    return first_term - second_term

def times(first_term, second_term):
    return first_term * second_term

def divide(first_term, second_term):
    return first_term / second_term

